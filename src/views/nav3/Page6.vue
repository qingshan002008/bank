	<section>
		<!--工具条-->
		<el-col :span="24" class="toolbar" style="padding-bottom: 0px;">
			<el-form :inline="true" :model="message">
				<el-form-item>
					<el-input v-model="message.title" placeholder="公告标题"></el-input>
				</el-form-item>
				<el-form-item>
					<el-button type="primary" v-on:click="getMessage">查询</el-button>
				</el-form-item>
			</el-form>
		</el-col>

		<!--列表-->
		<el-table :data="messages" highlight-current-row v-loading="listLoading" @selection-change="selsChange" style="width: 100%;">
			<el-table-column type="selection" width="55">
			</el-table-column>
			<el-table-column type="index" width="60">
			</el-table-column>
			<el-table-column prop="title" label="公告标题" style="width: 50%;"  sortable >
			</el-table-column>
			<el-table-column prop="auther" label="发布者" style="width: 20%;" :formatter="formatSex" sortable>
			</el-table-column>
			<el-table-column prop="date" label="时间" style="width: 30%;" sortable>
			</el-table-column>
			<el-table-column label="操作" width="150">
				<template scope="scope">
					<el-button size="small" @click="handleEdit(scope.$index, scope.row)">编辑</el-button>
					<el-button type="danger" size="small" @click="handleDel(scope.$index, scope.row)">删除</el-button>
				</template>
			</el-table-column>
		</el-table>

		<!--工具条-->
		<el-col :span="24" class="toolbar">
			<el-button type="danger" @click="handleAdd" >新增</el-button>
			<el-button type="danger" @click="batchRemove" >批量删除</el-button>
			<el-pagination layout="prev, pager, next" @current-change="handleCurrentChange" :page-size="20" :total="total" style="float:right;">
			</el-pagination>
		</el-col>

		<!--编辑界面-->
		<el-dialog title="编辑" :visible.sync="editFormVisible" :close-on-click-modal="false">
			<el-form :model="editForm" label-width="80px" :rules="editFormRules" ref="editForm">
				<el-form-item label="公告标题" prop="name">
					<el-input v-model="editForm.title" auto-complete="off"></el-input>
				</el-form-item>
				<!-- <el-form-item label="性别">
